// ===== APPLY LANGUAGE =====
function applyLang(lang) {
  const t = translations[lang];
  if (!t) return;

  document.documentElement.lang = lang;
  document.title = t.page_title;

  // Text nodes
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // HTML nodes (contain tags like <strong>, <br>, <span>)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // Testimonial avatars — first letter of name
  ['rev1', 'rev2', 'rev3'].forEach(id => {
    const el = document.getElementById(id + '_avatar');
    if (el && t[id + '_name']) el.textContent = t[id + '_name'][0].toUpperCase();
  });

  // Active button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  localStorage.setItem('kalyna_lang', lang);
}

